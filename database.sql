USE isnccmailup;

CREATE TABLE mail_status (
  id INTEGER PRIMARY KEY AUTO_INCREMENT,
  status TINYINT(1),
  time TIMESTAMP DEFAULT NOW()
) ENGINE InnoDb CHARACTER SET UTF8;

CREATE TABLE main_site_status (
  id INTEGER PRIMARY KEY AUTO_INCREMENT,
  status TINYINT(1),
  time TIMESTAMP DEFAULT NOW()
) ENGINE InnoDb CHARACTER SET UTF8;
